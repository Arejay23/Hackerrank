SinglyLinkedListNode* reverse(SinglyLinkedListNode* head) {

    if(head == nullptr){
        return head;
    }else{
        SinglyLinkedListNode* current, *prev, *fwd;
        current = head;
        prev = nullptr;
        fwd = nullptr;
        while(current != nullptr){
            fwd = current -> next;
            current -> next = prev;
            prev = current;
            current = fwd;
        }
        head = prev;
        return head;
    }


}

